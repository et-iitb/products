								<h3>Target Users</h3>
								<ol>
									<li><h4>Who can use LOBE ?</h4>
										<ul>
											<li>Quality control team of e-learning companies to evaluate the quality of learning objects produced.</li>
											<li>Subject matter experts, instructional designers and graphic designers to give meaningful feedback on quality of learning objects.</li>
											<li>Educators (i.e. Teachers, principals and HODs) to select good-quality learning objects.</li>
										</ul>
									</li>
									<li class="lobe6"><h4>When to use LOBE ?</h4>
										<p>If you are an e-learning company, you can use LOBE when you want to :</p>
										<ol type="a">
											<li>Find the quality assessment scores for your learning objects in terms of Content quality, Pedagogical alignment, Design efficacy and Technology integration</li>
											<li>Do strength-weakness analysis for a set of learning objects i.e.
												<ul>
													<li>Identify the weak spots along each of the quality dimensions (Content quality, Pedagogical alignment, Design efficacy, Technology integration) in your learning objects that need to be addressed to improve quality.</li>
													<li>Identify the strong points of your learning objects within each of the dimensions.</li>
													
												</ul>
											</li>
											
										</ol>

										<p>If you are an educator (HOD/Principal of a school), you can use LOBE when you want to :</p>
										
												<ul>
													<li>Compare between multiple learning objects from different repositories.</li>
													<li>Evaluate the quality of learning objects from a single repository.</li>
													
												</ul>
											
											
