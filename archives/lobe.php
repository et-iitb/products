						<h3>Learning object evaluation instrument (LOBE)</h3>

						<ol>
							<!-- 1  -->
							<li><h4>What is LOBE instrument ?</h4>
								<ul>
									<li>LOBE is a 4-scale rubric to evaluate the teaching-learning effectiveness of existing e-learning content i.e. learning objects.</li>
									<li>A learning object is a multimedia teaching-learning resource that contains dynamic visualizations with voice-over (i.e. animations/simulations/videos) as its core component and either all or a subset of the following components :
										<ul style="padding-left: 20px">
											<li>dynamic visualizations with voice-over (i.e. animations/simulations/videos).</li>
											<li>activities.</li>
											<li>set of assessment questions.</li>
											<li>solved example.</li>
											<li>stepwise lesson plans for teachers.</li>
										</ul>
									</li>
									<li>LOBE measures the quality of the learning objects along four dimensions - Content quality (7 questions), Pedagogical alignment (16 questions), Design efficacy (10 questions) and Technology integration (5 questions).The output from the rubric are questionwise scores under each dimension, for a particular learning object.</li>
								</ul>
							</li>
							<!-- 2 -->
							<li class="lobe2">
								<h4>What is the structure of LOBE ?</h4>
								<br>
								<img src="img/lobe_structure.jpg" width="70%">
								<span  style="padding-left: 400px"><i style="font-size: 14px;">Figure 1: Structure of LOBE</i></span>
								<span id="dots">...</span><span id="more">
								<ol type="A">
									<li>What quality dimensions are measured by LOBE ?
										<p>The four quality dimensions measured by LOBE are:</p>
										<ol type="a">
											<li>Content quality (C) (6 questions)</li>
											<li>Pedagogical alignment (P) (16 questions)</li>
											<li>Design efficacy (D) (10 questions)</li>
											<li>Technology integration (T) (8 questions) Quality dimensions measured by LOBE</li>

										</ol>
										<br>
										<table cellspacing="0" width="80%">
											<tr style="background-color: #29517f; color: #fff">
												<th>Quality dimensions measured by LOBE</th>
												<th>Description of dimension</th>
											</tr>
											<tr>
												<td>1.Content Quality (C)</td>
												<td>Measures accuracy of content and assessment questions, how well these components connect to real-world scenarios, 
												how updated the content is with recent advances in the topic knowledge etc.</td>
											</tr>
											<tr>
												<td>2.Pedagogical Alignment (P)</td>
												<td>Measures the extent to which the learning object is learner-centred and promotes higher-order thinking among the learners.</td>
											</tr>
											<tr>
												<td>3.Design efficacy (D)</td>
												<td>Measures the extent to which the learning object incorporates Multimedia design principles as well as Interaction design principles to promote meaningful learning among the learners. </td>
											</tr>
											<tr>
												<td>4.Technology Integration (T)</td>
												<td>Measures how well the learning object adheres to graphic design principles and how well the different components of the learning object have been integrated to create a meaningful teaching-learning experience.</td>
											</tr>
										</table>

										<br>
									</li>
							
									<li>What are the features of the questions in LOBE ?
										
										<ol type="a">
											<li>Questions in LOBE are organized along the three dimensions.</li>
											<li>Each question in LOBE rubric :
												<ul>
													<li>Measures a particular aspect of the specific dimension.</li>
													<li>Specifies which components of the learning object need to be considered for evaluating this question.</li>
													<li>Is accompanied by explanations on why this question is important for quality evaluation.</li>
												</ul>
											</li>
										</ol>
									</li>
								</ol>
							</li>
								</span>
								<a onclick="myFunction()" id="myBtndown" class="downloadb">Read more</a>
							<li class="lobe3">
								<h4>What is the scoring mechanism in LOBE ?</h4>
								<p>Each question can be scored along a 4-point scale : Score 0 (Missing); Score 1 (Inadequate); Score 2 (Almost) and Score 3 (Target)

								Score</p>
								<table cellspacing="0">
									<tr style="background-color: #29517f; color: #fff">
										<th>Score</th>
										<th>Means the quality aspect addressed by the question</th>
									</tr>
									<tr>
										<td>Score 0 (Missing)</td>
										<td>Is completely missing in the learning object</td>
									</tr>
									<tr>
										<td>Score 1 (Inadequate)</td>
										<td>Has been inadequately implemented in the learning object, needs lot of improvement</td>
									</tr>
									<tr>
										<td>Score 2 (Almost)</td>
										<td>Has almost been adequately implemented but there is still scope of improvement</td>
									</tr>
									<tr>
										<td>Score 3 (Target)</td>
										<td>Has been implemented in the learning object in the ideal way i.e. as recommended by educational technology research</td>
									</tr>
								</table>

								<ol type="a">
									<li>The quality level corresponding to each score is described in detail by the descriptors.</li>
									<li>Descriptors are often accompanied by examples. These examples are meant to further assist the evaluator in deciding the level of implementation quality for that question.</li>
								</ol>
								
							</li>
							<li class="lobe4"><h4>What is the scope of LOBE ?</h4>
								<p>LOBE is scoped to learning objects which have dynamic visualizations (simulations/animations/videos) as their core component. 
								Also,</p>
								<ol type="a">
									<li>LOBE evaluates learning objects after they have been created.</li>
									<li>The granularity level of a learning object is taken as a single topic from a textbook chapter, that typically contains 4 and 6 topics</li>
									<li>LOBE is currently designed for topics in STEM domains</li>
									<li>It has currently been tested for various topics at primary, secondary and high school levels. It may be applicable for college-level STEM topics but has not been tested yet.</li>
									<li>LOBE does not evaluate criterion like 'metadata retrieval'.</li>
								</ol>
							</li>
						</li>
					</ol>

						


